# Когда используется критерий Стьюдента, а когда Z –критерий?


# Z-критерий для двух выборок проверяет, отличаются ли две выборки. 
# Он обычно используется, когда два нормальных распределения имеют известные изменения, 
# и когда в эксперименте используется большой размер выборки.


